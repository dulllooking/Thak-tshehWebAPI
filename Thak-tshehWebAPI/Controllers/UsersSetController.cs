using System;
using System.Collections.Generic;
using System.Data;
using System.Data.Entity;
using System.Linq;
using System.Net;
using System.Web;
using System.Web.Mvc;
using Thak_tshehWebAPI.Models;

namespace Thak_tshehWebAPI.Controllers
{
    public class UsersSetController : Controller
    {
        private ApplicationDbContext db = new ApplicationDbContext();

        // GET: UsersSet
        public ActionResult Index()
        {
            return View(db.User.ToList());
        }

        // GET: UsersSet/Details/5
        public ActionResult Details(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            User user = db.User.Find(id);
            if (user == null)
            {
                return HttpNotFound();
            }
            return View(user);
        }

        // GET: UsersSet/Create
        public ActionResult Create()
        {
            return View();
        }

        // POST: UsersSet/Create
        // 若要免於大量指派 (overposting) 攻擊，請啟用您要繫結的特定屬性，
        // 如需詳細資料，請參閱 https://go.microsoft.com/fwlink/?LinkId=317598。
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Create([Bind(Include = "Id,Account,HashPassword,Salt,Image,Name,NickName,BirthDate,Sex,MobilePhone,Country,City,Area,FacebookLink,InstagramLink,AboutMe,MySkill,MyInterest,ShowComingActivity,ShowNeedEvalActivity,ShowCollectActivity,ShowFinishActivity,ShowCancelActivity,ShowFollowing,ShowFollowers,Views,AccountState,CreatDate,CheckMailCode,MailCodeCreatDate,RefreshToken,RefreshTokenCreatDate")] User user)
        {
            if (ModelState.IsValid)
            {
                db.User.Add(user);
                db.SaveChanges();
                return RedirectToAction("Index");
            }

            return View(user);
        }

        // GET: UsersSet/Edit/5
        public ActionResult Edit(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            User user = db.User.Find(id);
            if (user == null)
            {
                return HttpNotFound();
            }
            return View(user);
        }

        // POST: UsersSet/Edit/5
        // 若要免於大量指派 (overposting) 攻擊，請啟用您要繫結的特定屬性，
        // 如需詳細資料，請參閱 https://go.microsoft.com/fwlink/?LinkId=317598。
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Edit([Bind(Include = "Id,Account,HashPassword,Salt,Image,Name,NickName,BirthDate,Sex,MobilePhone,Country,City,Area,FacebookLink,InstagramLink,AboutMe,MySkill,MyInterest,ShowComingActivity,ShowNeedEvalActivity,ShowCollectActivity,ShowFinishActivity,ShowCancelActivity,ShowFollowing,ShowFollowers,Views,AccountState,CreatDate,CheckMailCode,MailCodeCreatDate,RefreshToken,RefreshTokenCreatDate")] User user)
        {
            if (ModelState.IsValid)
            {
                db.Entry(user).State = EntityState.Modified;
                db.SaveChanges();
                return RedirectToAction("Index");
            }
            return View(user);
        }

        // GET: UsersSet/Delete/5
        public ActionResult Delete(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            User user = db.User.Find(id);
            if (user == null)
            {
                return HttpNotFound();
            }
            return View(user);
        }

        // POST: UsersSet/Delete/5
        [HttpPost, ActionName("Delete")]
        [ValidateAntiForgeryToken]
        public ActionResult DeleteConfirmed(int id)
        {
            User user = db.User.Find(id);
            db.User.Remove(user);
            db.SaveChanges();
            return RedirectToAction("Index");
        }

        protected override void Dispose(bool disposing)
        {
            if (disposing)
            {
                db.Dispose();
            }
            base.Dispose(disposing);
        }
    }
}
